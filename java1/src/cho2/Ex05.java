package cho2;

public class Ex05 {
	public static void main(String[] args) {
		//비교연산자
		//( ==, >, <, >=, <=, ! )
		//비교연산자의 결과는 논리형으로 나온다
		
		System.out.println(10>0);
		System.out.println(10 == 2);
		System.out.println(10 == 1+10);
		System.out.println(10 != 0);
		
		// 변수 활용
		int a = 10, b = 0;
		System.out.println(a>=b);
		System.out.println(a==b+10);
		System.out.println(a != b);
	}
}
